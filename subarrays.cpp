ll maxSubArraySum(VI &a, ll size) 
{ 
   ll max_so_far = a[0]; 
   ll curr_max = a[0]; 
  
   for (int i = 1; i < size; i++) 
   { 
        curr_max = max(a[i], curr_max+a[i]); 
        max_so_far = max(max_so_far, curr_max); 
   } 
   return max_so_far; 
} 



/********** Main()  function **********/
int main()
{
    
    
    ll n = SI();
    VI v(n);
    REP(i,n){
    	v[i]=SI();
    }
    ll ans = maxSubArraySum(v,v.size());
    ll k = 0;
    ll curr_sum = 0;
    ll start_ptr;
    bool set = false;

    for(int i = 0; i < n; i++){
    	curr_sum+=v[i];
    	if(!set){
    		start_ptr = i;
    		set = true;
    	}
    	if(curr_sum==ans){
    		k++;
    		curr_sum=0;
    		set = false;
    	}
    	else if(curr_sum > ans){
    		curr_sum-=v[start_ptr];
    	}
    	else if(curr_sum < 0){
    		curr_sum=0;
    		set = false;
    	}
    }
    if(ans > 0){
    	cout<<ans<<" "<<k<<"\n";	
    }
    else{
    	cout<<0<<" "<<0<<"\n";
    }
    


    return 0;
}



